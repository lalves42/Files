#include <libft.h>

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	void	*temp;

	temp = malloc(len);
	if (temp == NULL)
		return (NULL);
	ft_memcpy(temp, src, len);
	ft_memcpy(dst, (const void*)temp, len);
	free(temp);
	return (dst);
}
